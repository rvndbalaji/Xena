<span id="tit"><strong>JavaScript is the stuff that makes Xena work properly.</strong></span>
<br><br>
There are ways you can enable JavaScript, and it depends on which browser you use. Following are steps to enable JavaScript for a few popular browsers.
<br><br>



<div id="gc">
<h3>Google Chrome</h3>
Support link : <a href="https://support.google.com/adsense/answer/12654?hl=en" target="_blank">https://support.google.com/adsense/answer/12654?hl=en</a>
<ol>
  <li>Click the Chrome menu icon <img src="//lh3.googleusercontent.com/tgmsrPRh34tM4q0WYv7l-NNXDz7dqm7ftk8TyCEguoY30xkEg8lcw_ULI23ki9Q=w18-h18" width="18" height="18" alt="Chrome menu" title="Chrome menu"> on the browser toolbar.</li>
  <li>Select <strong>Settings</strong>.</li>
  <li>On the "Settings" page, click the <strong>Show advanced settings...</strong> link.</li>
  <li>In the "Privacy" section, click <strong>Content settings...</strong></li>
  <li>Select <strong>Allow all sites to run JavaScript (recommended)</strong> in the "JavaScript" section.</li>
  <li>Click <strong>Done</strong>.</li>
</ol>
</div>
<br>
